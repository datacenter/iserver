class ContractFilterOutput():
    def __init__(self):
        pass

    def print_contract_filters(self, info, title=False):
        if title:
            self.my_output.default(
                'Contract Filter [#%s]' % (len(info)),
                underline=True,
                before_newline=True
            )

        if len(info) == 0:
            self.my_output.default('None')
            return

        order = [
            'faults',
            'nameTenant',
            'vzEntry.name',
            'vzEntry.etherT',
            'vzEntry.arpOpc',
            'vzEntry.prot',
            'vzEntry.applyToFrag',
            'vzEntry.stateful',
            'vzEntry.source',
            'vzEntry.destination',
            'vzEntry.tcpRules'
        ]

        headers = [
            'Faults',
            'Filter',
            'Entry',
            'Ether',
            'ARP Flag',
            'Proto',
            'Fragments',
            'Stateful',
            'Source',
            'Destination',
            'Rules'
        ]

        self.my_output.my_table(
            self.my_output.expand_lists(
                info,
                order,
                ['vzEntry']
            ),
            order=order,
            headers=headers,
            underline=True,
            row_separator=True,
            allow_order_subkeys=True,
            table=True
        )

